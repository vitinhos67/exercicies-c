/*
Crie um programa que, utilizando variáveis inteiras e os
números 6 e 3, faça e escreva a soma, a subtração, a
multiplicação e a divisão destes números.
*/

int main() {
    
    int soma = 6 + 3;
    int subtracao = 6 - 3;
    int multiplicacao = 6 * 3;
    int divisao = 8 / 3;

    printf("%d %d %d %d \n", soma, subtracao, multiplicacao, divisao);

    return 0;
}