/*
Crie um programa que, utilizando os números 8 e 2 faça e
escreva a soma, a subtração, a multiplicação e a divisão destes
números
*/

#include<stdio.h>
#include<stdlib.h>
#include<stdbool.h>



int main() {
    
    int soma = 8 + 2;
    int subtracao = 8 - 2;
    int multiplicacao = 8 * 2;
    int divisao = 8 / 2;

    printf("%d %d %d %d \n", soma, subtracao, multiplicacao, divisao);

    return 0;
}